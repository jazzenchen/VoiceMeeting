const DICTIONARIES = {
  zh: {},
  en: EN,
};

const I18nContext = createContext({
  locale: "zh",
  t: (key, values) => interpolate(key, values),
});

function interpolate(template, values = {}) {
  return String(template).replace(/\{(\w+)\}/g, (match, key) => (
    Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : match
  ));
}

export function clampLocale(value) {
  return SUPPORTED_LOCALES.includes(value) ? value : "zh";
}

export function loadLocale() {
  try {
    return clampLocale(window.localStorage.getItem(LOCALE_STORAGE_KEY) || "zh");
  } catch {
    return "zh";
  }
}

export function saveLocale(locale) {
  try {
    window.localStorage.setItem(LOCALE_STORAGE_KEY, clampLocale(locale));
  } catch {
    // Locale persistence is best-effort.
  }
}

export function I18nProvider({ locale, children }) {
  const value = useMemo(() => {
    const currentLocale = clampLocale(locale);
    const dictionary = DICTIONARIES[currentLocale] || {};
    return {
      locale: currentLocale,
      t: (key, values) => interpolate(dictionary[key] || key, values),
    };
  }, [locale]);

  return (
    <I18nContext.Provider value={value}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
